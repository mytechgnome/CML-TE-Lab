import telnetlib

def login():
    tn.read_until(b"Username: ")
    tn.write(b"cisco\n")
    tn.read_until(b"Password: ")
    tn.write(b"cisco\n")

def lo0():
    tn.read_until(b"#")
    tn.write(b"conf t\n")
    tn.read_until(b"#")
    tn.write(b"interface Lo0\n")

def finish():
    tn.write(b"end\n")
    tn.write(b"wr\n")
    tn.write(b"exit\n")
    print(tn.read_all().decode('ascii'))

